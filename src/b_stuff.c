#include "b_stuff.h"
/*============================================================================*/
bchar bStuff_FormatBuffer[2048];
/*============================================================================*/
bchar *bStuff_FormatString(bchar *_format, ...)
{
	va_list data;

	va_start(data, _format);
	_vsnwprintf(bStuff_FormatBuffer, 2048, _format, data);
	va_end(data);

	return bStuff_FormatBuffer;
}