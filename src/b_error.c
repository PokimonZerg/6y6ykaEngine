bvoid berror(bchar *_file, blong _line, bchar *_text)
{
	bchar text[512];

	swprintf(text, 512, L"Fatal error detected!\nfile: %ls\nline: %lld\ntext: %ls", 
		_file, _line, _text);

	if(bLog_Exist())
		bLog_Print(text, bLog_Error);

	MessageBox(NULL, text, L"6y6yka engine", MB_OK | MB_ICONERROR);

	exit(0);
}